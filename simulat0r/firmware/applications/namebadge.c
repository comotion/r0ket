void main_namebadge(void) {
    backlightInit();
    font=&Font_7x8;
    
    reload_content = &main_pane;
    
    while (1) {
        lcdDisplay();
        lcdFill(0);
        if(gpioGetValue(RB_BTN0)==0){
            set_mode(0);
        }
        if(gpioGetValue(RB_BTN1)==0){
            set_mode(1);
        }
        if(gpioGetValue(RB_BTN2)==0){
            set_mode(2);
        }
        if(gpioGetValue(RB_BTN3)==0){
            set_mode(3);
        }
        if(gpioGetValue(RB_BTN4)==0){
            set_mode(4);
        }
        delayms(100);
    }
}

void set_mode(int mode) {
    switch(mode) {
    case 0:
        reload_content = &left_pane;
        break;
    case 1:
        reload_content = &right_pane;
        break;
    case 2:
        reload_content = &bottom_pane;
        break;
    case 3:
        reload_content = &top_pane;
        break;
    case 4:
        reload_content = &main_pane;
        break;
    }
    reload_content();
}

void draw_image(int xoffs, int yoffs) {
    for(int y=0; y < 39; y++) {
        for (int x=0; x < 64; x++) {
            if ( nyancat[y][x] == 1) {
                lcdSafeSetPixel((x+xoffs), (y+yoffs), true);
            } else {
                lcdSafeSetPixel((x+xoffs), (y+yoffs), false);
            }
        }
    }
}
